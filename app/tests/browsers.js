/** DISPLAY **/
function list_browsers()
{
	var rows= '';
	var output= '<table>';
	$.each(browsers, function(index, value){
		var checked= '';
		if(value.enabled== true)
		{
			checked= 'checked';	
		}
		rows+= '<tr><td>'+value.platform+'</td><td>'+value.browserName+' '+value.version+'</td><td><input type="checkbox" '+checked+'/></td></tr>';
	});
	output+= rows;
	output+= '</table>';
	$('#browsers').html(output);
}

